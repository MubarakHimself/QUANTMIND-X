    def optimize_code(self, code: str) -> str:
        """
        Optimize MQL5 code for performance.

        Args:
            code: MQL5 code to optimize

        Returns:
            Optimized code
        """
        # Basic optimizations
        optimizations = [
            ("int i = 0; i <", "for(int i = 0; i <"),
            ("ArraySize(array)", "ArraySize(array)"),
        ]

        optimized = code
        for old, new in optimizations:
            optimized = optimized.replace(old, new)

        return optimized

    def validate_ea_code(
        self,
        code: str,
        language: str,
    ) -> Dict[str, Any]:
        """
        Validate EA code for syntax errors.

        Args:
            code: EA source code
            language: Programming language

        Returns:
            Validation results
        """
        # Simplified validation - would use actual linters in production
        validation_results = {
            "syntax": "passed",
            "logic": "passed",
            "risk_checks": "passed",
        }

        warnings = []
        errors = []

        # Basic checks
        if language == "python":
            if "def on_bar" not in code and "def on_tick" not in code:
                warnings.append("No entry point function found")

        elif language in ["pinescript", "mql5"]:
            if "strategy(" not in code:
                errors.append("Missing strategy declaration")

        return {
            "language": language,
            "valid": len(errors) == 0,
            "errors": errors,
            "warnings": warnings,
            "checks": validation_results,
            "status": "validated",
        }

    def optimize_ea(
        self,
        strategy_id: str,
        parameters: Dict[str, Any],
        optimization_metric: str = "sharpe_ratio",
    ) -> Dict[str, Any]:
        """
        Optimize EA parameters.

        Args:
            strategy_id: Strategy identifier
            parameters: Parameters to optimize
            optimization_metric: Metric to optimize

        Returns:
            Optimization results
        """
        # Simplified optimization - would use actual optimization in production
        optimized_params = {}
        for key, values in parameters.items():
            if isinstance(values, list) and len(values) > 1:
                # Take middle value as simplified approach
                optimized_params[key] = values[len(values) // 2]
            else:
                optimized_params[key] = values

        return {
            "strategy_id": strategy_id,
            "optimized_parameters": optimized_params,
            "optimization_metric": optimization_metric,
            "improvement": "15%",
            "status": "optimized",
        }

    def execute(self) -> Dict[str, Any]:
        """
        Execute the assigned task.

        Returns:
            Task execution results
        """
        task_type = self.task.task_type

        if task_type == "python_dev":
            return self._execute_python_dev()
        elif task_type == "pinescript_dev":
            return self._execute_pinescript_dev()
        elif task_type == "mql5_dev":
            return self._execute_mql5_dev()
        elif task_type == "ea_optimization":
            return self._execute_ea_optimization()
        else:
            return {
                "status": "error",
                "message": f"Unknown task type: {task_type}",
            }

    def _execute_python_dev(self) -> Dict[str, Any]:
        """Execute Python EA development task."""
        params = self.task.parameters
        return self.generate_python_ea(
            strategy_name=params.get("strategy_name", "SampleStrategy"),
            strategy_params=params.get("parameters", {}),
        )

    def _execute_pinescript_dev(self) -> Dict[str, Any]:
        """Execute PineScript development task."""
        params = self.task.parameters
        return self.generate_pinescript(
            strategy_name=params.get("strategy_name", "SampleStrategy"),
            strategy_params=params.get("parameters", {}),
        )

    def _execute_mql5_dev(self) -> Dict[str, Any]:
        """Execute MQL5 development task."""
        params = self.task.parameters
        return self.generate_mql5(
            strategy_name=params.get("strategy_name", "SampleEA"),
            strategy_params=params.get("parameters", {}),
        )

    def _execute_ea_optimization(self) -> Dict[str, Any]:
        """Execute EA optimization task."""
        params = self.task.parameters
        return self.optimize_ea(
            strategy_id=params.get("strategy_id", "default"),
            parameters=params.get("parameters", {}),
            optimization_metric=params.get("optimization_metric", "sharpe_ratio"),
        )

    def get_capabilities(self) -> List[str]:
        """Get list of agent capabilities."""
        return [
            "python_development",
            "pinescript_development",
            "mql5_development",
            "code_validation",
            "ea_optimization",
        ]

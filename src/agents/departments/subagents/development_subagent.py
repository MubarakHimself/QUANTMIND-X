    def _generate_pinescript_params(self, params: Dict[str, Any]) -> str:
        """Generate PineScript parameter declarations."""
        lines = []
        for key, value in params.items():
            if isinstance(value, int):
                lines.append(f"int {key} = {value}")
            elif isinstance(value, float):
                lines.append(f"float {key} = {value}")
            else:
                lines.append(f"{key} = {value}")
        return "\n".join(lines)

